    def add(self, a, b):
        result = 0
        result = a + b
        return result

    def subtract(self, a, b):
        result = 0
        result = a - b
        return result

    def multiply(self, a, b):
        result = 0
        result = a * b
        return result

    def divide(self, a, b):
        result = 0
        result = a / b
        return result